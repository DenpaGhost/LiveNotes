public static class gameParameters{

    private static uint max, min, bpm, speed, phrase, repeat;

    public static uint Max
    {
        get
        {
            return max;
        }

        set
        {
            max = value;
        }
    }

    public static uint Min
    {
        get
        {
            return min;
        }

        set
        {
            min = value;
        }
    }

    public static uint Bpm
    {
        get
        {
            return bpm;
        }

        set
        {
            bpm = value;
        }
    }

    public static uint Speed
    {
        get
        {
            return speed;
        }

        set
        {
            speed = value;
        }
    }

    public static uint Phrase
    {
        get
        {
            return phrase;
        }

        set
        {
            phrase = value;
        }
    }

    public static uint Repeat
    {
        get
        {
            return repeat;
        }

        set
        {
            repeat = value;
        }
    }
}
